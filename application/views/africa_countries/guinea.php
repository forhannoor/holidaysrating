<h1>Flora and Fauna</h1>
<div class="edit4">
<p>The typical vegetation of Lower Guinea is woodland and on the higher plateaus and peaks are dense forests with plants found nowhere else in the world. Savanna woodland characterizes Upper Guinea, with only tall grass in large areas. Dense mangrove forests grow along the river mouths and there is rain forest along the border with Liberia. 
Wildlife includes the elephant, buffalo, hippopotamus, lion, leopard, and many kinds of antelope and monkey are to be found in Guinea, as well as crocodiles and several species of venomous snakes. It is also a birdwatching paradise with all kinds of colourfull birds.</p>
</div>


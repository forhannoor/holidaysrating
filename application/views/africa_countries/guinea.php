<h1>Climate</h1>
<div class="edit3">
<p>The coastal region of Guinea and most of the inland have a tropical climate, with a rainy season from April to November, relatively high temperatures, Southwesterly winds, and high humidity.<br />
<a href="http://www.holidaysrating.com/index.php/africa_cities/conakry">Conakry</a>, the capital and largest city, has year-round temperatures between 23°C and 29°C. The average annual rainfall is 4,300 mm. Upper Guinea has a shorter rainy season and greater daily temperature variations. The dry season, which continues from December to May has Northeasterly harmattan winds.</p>
</div>


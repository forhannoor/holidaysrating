<h1>Climate</h1>
<div class="edit2">
<p>The climate is typically equatorial, with high temperatures and heavy rainfall. The wet seasons in the continental region are from February to June and from September to December the rainiest months in Bata are September, October. Sometimes the rain reaches 180 inches (4,600 mm) in November at Calatrava, farther south on the coast. The temperature at Malabo, Bioko, ranges from 16°C to 33°C, while on the southern Moka Plateau normal high temperatures are only 21°C. In Río Muni, the common temperature is about 27°C.</p>
</div>


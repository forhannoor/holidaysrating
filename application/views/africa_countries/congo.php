<h1>Introduction</h1>
<div class="flag" style="float:left"><img src="<?php echo base_url() ?>assets/images/flags/africa/congo.jpg" alt="Congo" /></div>

<div class="edit1">
<p><strong>Republic of Congo</strong> also referred to as Congo-Brazzaville, is a country located in central <a href="http://www.holidaysrating.com/index.php/regions/africa">Africa</a> and has a size of 342,000 square kilomtres. Not to be confused with <a href="http://www.holidaysrating.com/index.php/africa_countries/democratic_republic_of_congo">Democratic Republic of Congo</a> or <a href="http://www.holidaysrating.com/index.php/africa_countries/democratic_republic_of_congo">Congo-Kinshasa</a><br><br>
Famous for its rich history and welcoming culture, the Republic of Congo has a well-developed tourist infrastructure. Whether you’re interested in safaris to spot wildlife, hiking through the rain forest, enjoying the sunny beaches or explore the streets of vibrant <a href="http://www.holidaysrating.com/index.php/africa_cities/brazzaville">Brazzaville</a>.<br><br>
The capital, <a href="http://www.holidaysrating.com/index.php/africa_cities/brazzaville">Brazzaville</a>, is located on the Congo River, in the South of the country, immediately across from <a href="http://www.holidaysrating.com/index.php/africa_cities/kinshasa">Kinshasa</a>, the capital of the <a href="http://www.holidaysrating.com/index.php/africa_countries/democratic_republic_of_congo">Democratic Republic of Congo</a>.
Most of the population is concentrated in the Southwestern region of the country, leaving the vast areas of tropical jungle in the north virtually uninhabited. Thus, Congo is one of the most urbanized countries in Africa, with 85% of its entire population living in a few urban areas.
French is known to be the official language of Congo but other indigenous languages are also regularly spoken.
</p>

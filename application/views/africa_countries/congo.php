<h1>Flora and Fauna</h1>
<div class="edit3">
<p>The Southwest of the country is a coastal plain for which the primary drainage is the Kouilou-Niari River; the interior of the country has central plateau between two basins to the North and South. A Bigger part of the country is covered with tropical rainforest but they are under serious misuse  and pressure. The dense growth of red cedar, African oak, walnut, softwood okoumé, or gaboon mahogany, and hardwood limba are seen in some regions.
 Mangrove forests, coconut palms, and tall grasses grow in the coastal regions and eastern swamps. The plateaus and the Niari valley are covered with grasses and scattered broad-leaved trees.

Many kinds of monkeys, chimpanzees, gorillas, elephants, okapis, wild boars, and buffaloes find their living in the forests. Wildlife in the savanna regions embraces antelopes, jackals,  hyenas, wild dogs and cheetahs. Rhinoceroses and giraffes are numerous on the plateaus, but lions are hardly seen. Predatory eagles, hawks, and scavenging vultures,  owls, and wading herons  are some of the common birds. 
</p>
</div>


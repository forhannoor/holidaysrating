<div class="edit2"><p>Togo has a tropical climate. In the south the rainy seasons occur from mid-April through June and from mid-September through October. The narrow coastal zone, which receives about 35 inches (890 mm) of precipitation annually, is the driest region. The area near Palimé, about 65 miles (100 km) inland, receives the highest amount of precipitation—about 70 inches (1,800 mm) annually. </p></div>


<h1>Flora and Fauna</h1>
<div class="edit4"><p>Low-rainfall savannas consist of grasses and thorny trees. Acacia trees dominate these savannas, with one species, A. senegal, yielding gum arabic, which was long one of Sudan’s principal exports. The high-rainfall savannas of the south-central part of the country are more lush, with rich grasses along the Nile that support a large number of cattle. The intermittent woodlands dotting this belt gradually merge southward with the true rainforest that is now found only in remnants in the southernmost portions of the country. </p></div>


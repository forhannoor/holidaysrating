<h1>Climate</h1>
<div class="edit3"><p>South Sudan’s lowest annual temperatures are in the high 60s to high 70s F (low to mid-20s C) in the centre and north of the country, while the lowest annual temperatures in the outlying areas are slightly cooler, about high 50s to high 60s F (mid-10s to low 20s C). The highest annual temperatures for most of South Sudan generally range from the mid-80s to the mid-90s F (low to mid-30s C), although some areas toward the north of the country range from the mid-90s to mid-100s F (mid- to upper 30s C). The highest temperatures normally occur just before the rainy season. </p></div>


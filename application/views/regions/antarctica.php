<p><strong>Antarctica</strong> is the Southernmost continent of the World, containing the geographic South Pole. Antarctica consists of a vast, ice-covered ocean surrounded by treeless permafrost.<br /><br />
On average Antarctica, is the coldest, driest, and windiest continent, and has the highest average elevation of all the continents. 
Antarctica can be classified as a desert by definition. A Desert is defined as a region that has less than 254mm (10 inch) of annual rainfall.</p>
<p>Unlike other deserts, there is little evaporation from Antarctica, so the relatively little snow that does fall, doesn't go away again. Instead it builds up over hundreds and thousands of years into enormously thick ice sheets.
Heavy snowfalls occur when cyclonic storms pick up moisture from the surrounding seas and then deposit this moisture as snow along the coasts.<br /><br />
There are no permanent human residents, but anywhere from 1,000 (in winter) to 5,000 (in summer) people reside throughout the year at the research stations scattered across the continent. 
To enjoy a lifetime experience, we recommand you to take a cruise to this extreme continent.</p>
